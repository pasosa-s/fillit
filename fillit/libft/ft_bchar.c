#include "libft.h"

void	ft_bchar(void *s, size_t n, char c)
{
	size_t		i;

	i = 0;
	while (i < n - 1)
	{
		((char *)s)[i] = c;
		i++;
	}
	((char *)s)[i] = 0;
}
